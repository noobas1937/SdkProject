package com.chatsdk.model.mail.battle;

public class ArmyTotalParams
{
	private int	num;
	private int	hurt;
	private int	kill;
	private int	dead;
	private int	rescue;

	public int getNum()
	{
		return num;
	}

	public void setNum(int num)
	{
		this.num = num;
	}

	public int getHurt()
	{
		return hurt;
	}

	public void setHurt(int hurt)
	{
		this.hurt = hurt;
	}

	public int getKill()
	{
		return kill;
	}

	public void setKill(int kill)
	{
		this.kill = kill;
	}

	public int getDead()
	{
		return dead;
	}

	public void setDead(int dead)
	{
		this.dead = dead;
	}

	public int getRescue()
	{
		return rescue;
	}

	public void setRescue(int rescue)
	{
		this.rescue = rescue;
	}
}
